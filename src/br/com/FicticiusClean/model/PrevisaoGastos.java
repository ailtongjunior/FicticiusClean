package br.com.FicticiusClean.model;

public class PrevisaoGastos {
	private float PrecoGas;
	private float KmCidade;
	private float KmRodovia;
	
	public float getPrecoGas() {
		return PrecoGas;
	}
	public void setPrecoGas(float precoGas) {
		PrecoGas = precoGas;
	}
	
	public float getKmCidade() {
		return KmCidade;
	}
	public void setKmCidade(float kmCidade) {
		KmCidade = kmCidade;
	}
	
	public float getKmRodovia() {
		return KmRodovia;
	}
	public void setKmRodovia(float kmRodovia) {
		KmRodovia = kmRodovia;
	}	
}
